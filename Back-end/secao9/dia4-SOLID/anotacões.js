// Os princípios SOLID
// A palavra SOLID (sólido) no contexto de programação, é um acrônimo para cincos princípios que, se aplicados de maneira conjunta e inteligente, geram solidez e durabilidade para sua arquitetura como um todo.

// Single responsability principle (Princípio da responsabilidade única): uma entidade (classe, método, função, etc) deve ter apenas uma única responsabilidade;
// Open/Closed principles ( Princípio de substituição de Liskov): objetos em um programa devem ser substituíveis por instâncias de seus subtipos, sem alterar a funcionalidade do programa.
// Interface segregation principle ( Princípio da segregação da interface): Interfaces específicas são melhores do que uma única interface para todos os propósitos.
// Dependency inversion principle (princípio da inversão da dependência): entidades de alto nível não devem depender da entidade de baixo nível. Ambos devem depender de abstrações.

// Princípio => nada deve ser considerado obrigatório ou proibido, mas sim recomendado ou não.

// Os princípios SOLID e regras como a do EsLint geralmente são criadas para nos ajudar a escrever códigos bons.

