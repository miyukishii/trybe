// Tipos de queries:
// DDL - Data Definition Language: comando que lidam com o esquema, descrição e o modo como os dados existem em um banco de dados.
//       - CREATE: Para criar bancos de dados, tabelas, índices, views, procedures, funtions e triggers;
//       - ALTER: Para alterar a estrutura de qualquer objeto;
//       - DROP: Permite deletar objetos;
//       - TRUNCATE: Apenas esvaziar os dados dentro de uma tabelza, mas mantém no banco de dados.
// DML - Data Manipulation Language: comandos que são usados para manipular dados. São utilizados para armazenar, modificar, buscar e excluir dados em um banco de dados.
//       - SELECT: Usado para buscar dados em banco de dados;
//       - INSERT: Insere dados em uma tabela;
//       - UPDATE: Altera dados dentro de uma tabela;
//       - DELETE: Exclui dados de uma tabela;
// DCL - Data Control Language: comandos que concedem direitos, permissões e outros tipos de controle ao sistema de banco de dados.
//       - GRANT: Concede acesso a um usuário;
//       - REVOKE: Remove acessos concedidos através do comando GRANT;
// TCL - Transactional Control Language: Lida com as transações dentro de suas pesquisas.
//       - COMMIT: Muda suas alterações de temporárias para permanentes no banco de dados;
//       - ROLLBACK: Desfaz todo o impacto realizado por um comando;
//       - SAVEPOINT: Define pontos para os qais uma transação pode voltar.
//       - TRANSACTION: Definem onde, como e em que escopo suas transações são executadas.

